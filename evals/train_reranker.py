#!/usr/bin/env python3
"""Fine-tune a cross-encoder reranker on openHas memory recall data.

Uses sentence-transformers CrossEncoder with a multilingual base model.
Trains on (query, passage) → relevance_score pairs generated by gen_rerank_data.py.

Usage:
    python evals/gen_rerank_data.py          # generate training data first
    python evals/train_reranker.py           # train
    python evals/train_reranker.py --base-model BAAI/bge-reranker-v2-m3
    python evals/train_reranker.py --epochs 10 --lr 2e-5

Output: evals/reranker_model/ (saved cross-encoder, ready for inference)
"""

import argparse
import json
from pathlib import Path

import torch
from sentence_transformers import InputExample, CrossEncoder
from sentence_transformers.cross_encoder.evaluation import (
    CERerankingEvaluator,
)
from torch.utils.data import DataLoader

_DATA = Path(__file__).parent / "rerank_train.jsonl"
_OUT = Path(__file__).parent / "reranker_model"

# Small multilingual reranker — good balance of size and quality
_DEFAULT_BASE = "BAAI/bge-reranker-v2-m3"


def _load_data(path: Path) -> list[dict]:  # type: ignore[type-arg]
    rows = []
    with open(path) as f:
        for line in f:
            line = line.strip()
            if line:
                rows.append(json.loads(line))
    return rows


def _to_examples(rows: list[dict]) -> list[InputExample]:  # type: ignore[type-arg]
    return [
        InputExample(texts=[r["query"], r["passage"]], label=float(r["label"]))
        for r in rows
    ]


def _split_data(
    rows: list[dict], val_ratio: float = 0.15  # type: ignore[type-arg]
) -> tuple[list[dict], list[dict]]:  # type: ignore[type-arg]
    split = int(len(rows) * (1 - val_ratio))
    return rows[:split], rows[split:]


def _build_eval_samples(
    val_rows: list[dict],  # type: ignore[type-arg]
) -> list[dict]:  # type: ignore[type-arg]
    """Group val rows by query for CERerankingEvaluator format."""
    by_query: dict[str, dict[str, list[str]]] = {}
    for r in val_rows:
        q = r["query"]
        if q not in by_query:
            by_query[q] = {"query": q, "positive": [], "negative": []}
        if r["label"] > 0.5:
            by_query[q]["positive"].append(r["passage"])
        else:
            by_query[q]["negative"].append(r["passage"])
    return [v for v in by_query.values() if v["positive"] and v["negative"]]


def _parse_args() -> argparse.Namespace:
    p = argparse.ArgumentParser(description="Train cross-encoder reranker")
    p.add_argument("--base-model", default=_DEFAULT_BASE,
                   help=f"Base model (default: {_DEFAULT_BASE})")
    p.add_argument("--data", default=str(_DATA),
                   help="Training data JSONL path")
    p.add_argument("--out", default=str(_OUT),
                   help="Output model directory")
    p.add_argument("--epochs", type=int, default=5,
                   help="Training epochs (default: 5)")
    p.add_argument("--lr", type=float, default=2e-5,
                   help="Learning rate (default: 2e-5)")
    p.add_argument("--batch-size", type=int, default=16,
                   help="Batch size (default: 16)")
    return p.parse_args()


def main() -> None:
    args = _parse_args()
    data_path = Path(args.data)
    if not data_path.exists():
        print(f"Training data not found: {data_path}")
        print("Run: python evals/gen_rerank_data.py first")
        return

    rows = _load_data(data_path)
    train_rows, val_rows = _split_data(rows)
    print(f"Loaded {len(rows)} rows → {len(train_rows)} train, {len(val_rows)} val")

    device = "mps" if torch.backends.mps.is_available() else "cpu"
    print(f"Device: {device}")
    print(f"Base model: {args.base_model}")

    model = CrossEncoder(args.base_model, device=device)

    train_examples = _to_examples(train_rows)
    train_loader = DataLoader(train_examples, batch_size=args.batch_size, shuffle=True)

    eval_samples = _build_eval_samples(val_rows)
    evaluator = CERerankingEvaluator(samples=eval_samples, name="val")

    print(f"\nTraining for {args.epochs} epochs, lr={args.lr}, batch={args.batch_size}")
    print(f"Train examples: {len(train_examples)}, eval samples: {len(eval_samples)}")

    model.fit(
        train_dataloader=train_loader,
        epochs=args.epochs,
        optimizer_params={"lr": args.lr},
        evaluator=evaluator,
        evaluation_steps=len(train_loader),
        output_path=args.out,
        save_best_model=True,
    )

    print(f"\nModel saved to {args.out}")

    # Quick sanity check
    test_pairs = [
        ("Can the user eat butter?",
         "User has lactose intolerance, causes digestive discomfort"),
        ("Can the user eat butter?",
         "User plays jazz piano as a hobby, practices on weekends"),
    ]
    scores = model.predict(test_pairs)
    print("\nSanity check:")
    for (q, p), s in zip(test_pairs, scores):
        print(f"  {s:.4f}  {q[:40]} ↔ {p[:50]}")


if __name__ == "__main__":
    main()
